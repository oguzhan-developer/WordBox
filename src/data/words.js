// Get words by level
export const getWordsByLevel = (level) => {
    return wordsData.filter(word => word.level === level);
};

// Get word by id
export const getWordById = (id) => {
    return wordsData.find(word => word.id === parseInt(id));
};

// Get words by category
export const getWordsByCategory = (category) => {
    return wordsData.filter(word => word.category === category);
};

// Search words
export const searchWords = (query) => {
    const lowerQuery = query.toLowerCase();
    return wordsData.filter(word =>
        word.word.toLowerCase().includes(lowerQuery) ||
        word.turkish.toLowerCase().includes(lowerQuery) ||
        word.definition.toLowerCase().includes(lowerQuery)
    );
};

// Get random words for practice
export const getRandomWords = (count = 10, level = null) => {
    let words = level ? getWordsByLevel(level) : wordsData;
    return words.sort(() => Math.random() - 0.5).slice(0, count);
};
